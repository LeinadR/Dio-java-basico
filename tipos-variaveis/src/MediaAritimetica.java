public class MediaAritimetica {
   public static void main(String[] args) {
    
    double media1 = (8 + 9 + 7) / 3.0;
    double media2 = (4 + 5 + 6) / 3.0;

    double somaMedia = media1 + media2;

    double mediaMedias = somaMedia / 2.0;

    System.out.println("A media dos números 8, 9, 7 é: " + media1);
    System.out.println("A media dos números 4, 5, 6 é: " + media2);
    System.out.println("A soma das duas médias é: " + somaMedia);
    System.out.println("A media das duas médias é: " + mediaMedias);
   }
}
